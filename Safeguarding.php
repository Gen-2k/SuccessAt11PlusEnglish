                    <div class="card-body">
                        <p>Children are vulnerable to abuse by their peers. Peer-on-peer abuse is taken seriously by staff and will be subject to the same child protection procedures as other forms of abuse. Staff are aware of the potential uses of information technology for bullying and abusive behaviour between young people.</p>
                        <p>Staff will not dismiss abusive behaviour as normal between young people. The presence of one or more of the following in relationships between children should always trigger concern about the possibility of peer-on-peer abuse:</p>
                        <ul>
                            <li>Sexual activity (in primary school-aged children) of any kind, including sexting</li>
                            <li>One of the children is significantly more dominant than the other(s) (e.g. much older)</li>
                            <li>One of the children is significantly more vulnerable than the other(s) (e.g. in terms of disability, confidence, physical strength)</li>
                            <li>There has been some use of threats, bribes or coercion to ensure compliance or secrecy. If peer-on-peer abuse is suspected or disclosed, we will follow the same procedures as set out above for responding to child abuse.</li>
                        </ul>
