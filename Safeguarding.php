                    <div class="card-body">
                        <p>Child abuse is any form of physical, emotional or sexual mistreatment or lack of care that leads to injury or harm. An individual may abuse or neglect a child directly, or by failing to protect them from harm. Some forms of child abuse and neglect are listed below.</p>
                        <ul>
                            <li>Emotional abuse is the persistent emotional maltreatment of a child so as to cause severe and persistent adverse effects on the child's emotional development. It may involve making the child feel that they are worthless, unloved, or inadequate. Some level of emotional abuse is involved in all types of maltreatment of a child, though it may occur alone.</li>
                            <li>Physical abuse can involve hitting, shaking, throwing, poisoning, burning, drowning,suffocating or otherwise causing physical harm to a child. Physical harm may be also caused when a parent or carer feigns the symptoms of, or deliberately causes, ill health to a child.</li>
                            <li>Sexual abuse involves forcing or enticing a child to take part in sexual activities, whether or not the child is aware of what is happening. This can involve physical contact, or non-contact activities such as showing children sexual activities or encouraging them to behave in sexually inappropriate ways.</li>
                            <li>Neglect is the persistent failure to meet a child's basic physical and emotional needs. It can involve a failure to provide adequate food, clothing and shelter, to protect a child from physicaland emotional harm, to ensure adequate supervision or to allow access to medical treatment.</li>
                        </ul>
